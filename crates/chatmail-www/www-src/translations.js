/**
 * Apply translations to all elements with data-i18n attribute.
 * Elements with data-i18n-html will have innerHTML set instead of textContent.
 */
function applyTranslations(lang) {
    var t = TRANSLATIONS[lang] || TRANSLATIONS['en'];

    // Set document direction
    document.documentElement.dir = t.dir;
    document.documentElement.lang = lang;

    // Apply text translations
    var elements = document.querySelectorAll('[data-i18n]');
    for (var i = 0; i < elements.length; i++) {
        var key = elements[i].getAttribute('data-i18n');
        if (t[key] !== undefined) {
            elements[i].textContent = t[key];
        }
    }

    // Apply HTML translations (for content with inline HTML like <strong>, <code>)
    var htmlElements = document.querySelectorAll('[data-i18n-html]');
    for (var j = 0; j < htmlElements.length; j++) {
        var htmlKey = htmlElements[j].getAttribute('data-i18n-html');
        if (t[htmlKey] !== undefined) {
            htmlElements[j].innerHTML = t[htmlKey];
        }
    }

    // Update CSS for RTL/LTR
    if (t.dir === 'rtl') {
        document.body.classList.add('is-rtl');
        document.body.classList.remove('is-ltr');
    } else {
        document.body.classList.add('is-ltr');
        document.body.classList.remove('is-rtl');
    }
}

/**
 * Get a single translation string.
 */
function t(key) {
    var lang = document.documentElement.lang || 'en';
    var dict = TRANSLATIONS[lang] || TRANSLATIONS['en'];
    return dict[key] || key;
}
